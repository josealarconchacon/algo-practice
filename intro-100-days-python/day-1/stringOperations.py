# String Operations

# Concatenation
sayHello = "Hello, " + "Tom"
print(sayHello)

# Repetition
echo = "There!, " * 3
print(echo)

# Slicing
s = "Hello, world!"
print(s[0:5])  # Output: Hello
print(s[:5])   # Output: Hello
print(s[7:])   # Output: world!
print(s[-1])   # Output: !

# Length
print(len(s))  # Output: 13
